class IStringFinder {
public:
	virtual int Find(char* needle, char* haystack) = 0;
};

class BoyerMooreStringFinder: public IStringFinder {
public:
	BoyerMooreStringFinder() { };
	virtual ~BoyerMooreStringFinder() { };

public:
	virtual int Find(char* needle, char *haystack);
private:
	virtual void CreateTable(int* table, char* needle, int length);
};

